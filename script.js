// Part 1 - Fizz Bizz Problem
for (let i = 1; 1 <= 100; i++) {
  if (i % 3 == 0) {
    console.log(`${i} is a Fizz`);
  } else if (i % 5 == 0) {
    console.log(`${i} is a Bizz`);
  } else if (i % 3 == 0 && i % 5 == 0) {
    console.log(`${i} is a Fizz Bizz`);
  } else {
    console.log(`${i}`);
  }
}

