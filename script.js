for (let i = 1; i <= 100; i++) {
  if (i % 3 == 0 && i % 5 == 0) {
    console.log(`${i} is a Fizz Buzz`);
  } else if (i % 3 == 0) {
    console.log(`${i} is a Fizz`);
  } else if (i % 5 == 0) {
    console.log(`${i} is a Buzz`);
  } else {
    console.log(`${i}`);
  }
}

